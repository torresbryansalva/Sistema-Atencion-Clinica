    private void btn_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ingresarActionPerformed
        
        String usuario=txt_usuario.getText();
        String clave1=String.valueOf(txt_clave.getPassword());

        VerDatos vd= new VerDatos();
        String pass=vd.getPass(usuario);
        
        char letra = vd.ObtenerLetra(pass);
        
        switch (letra) {
            case 'E' -> {
                if(clave1.equals(pass)){
                Enfermera enf = new Enfermera();
                enf.setVisible(true);
                this.dispose();
                }
            }
            case 'D' -> {
                if(clave1.equals(pass)){
                clave2=String.valueOf(txt_clave.getPassword());
                Doctor doc=new Doctor();
                doc.setVisible(true);
                this.dispose();
                }
            }
            default -> {
                if(clave1.equals(pass)){
                Administrador admin = new Administrador();        
                admin.setVisible(true);
                this.dispose();
                }
            }
        }
    }//GEN-LAST:event_btn_ingresarActionPerformed

