    public void BuscarDatosDoctor(){
       
            String clave=Inicio.clave2;
            
            //txt_namedoctor.setText(clave);

            try {    
            String SQL_DNIDOCTOR="SELECT dni FROM dbclinica.permisos WHERE clave='" +clave+ "'";
            
            conexion=Conectar.getConnection();
            pstm =conexion.prepareStatement(SQL_DNIDOCTOR);
            rs=pstm.executeQuery();
            if(rs.next()){
               dnip=rs.getString("dni");
            } 
        } catch (SQLException ex) {
            Logger.getLogger(Doctor.class.getName()).log(Level.SEVERE, null, ex);
        }
        //    JOptionPane.showMessageDialog(null, dnip);
          
            String SQL_BUSCARDATOS="SELECT nombre,codempleado FROM dbclinica.empleados WHERE dni=" +dnip+ "";
     try {
            conexion=Conectar.getConnection();
            pstm =conexion.prepareStatement(SQL_BUSCARDATOS);
            rs=pstm.executeQuery();
            if(rs.next()){
            txt_namedoctor.setText(rs.getString("nombre"));
            txt_coddoc.setText(rs.getString("codempleado"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Doctor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
