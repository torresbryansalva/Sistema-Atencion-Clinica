    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
         int dni_buscar=Integer.parseInt(txt_dnibuscar.getText());
         
         String SQL_BUSCAR="SELECT * FROM dbclinica.datosclientes WHERE dni=" +dni_buscar+ "";
       try {
           conexion=Conectar.getConnection();
            pstm =conexion.prepareStatement(SQL_BUSCAR);
            rs=pstm.executeQuery();
            if(rs.next()){
                txt_dni.setText(rs.getString("dni"));
                txt_nombre.setText(rs.getString("nombre"));
                txt_apellido.setText(rs.getString("apellido"));
                txt_edad.setText(rs.getString("edad"));
                txt_telefono.setText(rs.getString("telefono"));
                txt_correo.setText(rs.getString("correo"));
            }else{
        JOptionPane.showMessageDialog(null, "No registrado");    
        }

       } catch (SQLException ex) {
           Logger.getLogger(Enfermera.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_btn_buscarActionPerformed

