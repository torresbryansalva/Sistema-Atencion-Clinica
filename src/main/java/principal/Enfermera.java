    private void btn_guardardatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardardatosActionPerformed
        
        int dni=Integer.parseInt(txt_dni.getText());
        String nombre=txt_nombre.getText();
        String apellido=txt_apellido.getText();
        int edad=Integer.parseInt(txt_edad.getText());
        int telefono=Integer.parseInt(txt_telefono.getText());
        String correo=txt_correo.getText();
        txt_dnibuscar.setText(String.valueOf(dni));
         String SQL_INSERTAR="INSERT INTO dbclinica.datosclientes(dni,nombre,apellido,edad,telefono,correo)VALUES(?,?,?,?,?,?)";

       try {
           conexion=Conectar.getConnection();
           pstm =conexion.prepareStatement(SQL_INSERTAR);
           pstm.setInt(1, dni);
           pstm.setString(2, nombre);
           pstm.setString(3,apellido);
           pstm.setInt(4, edad);
           pstm.setInt(5, telefono);
           pstm.setString(6, correo);
           int n=pstm.executeUpdate();
        if(n>0){
                JOptionPane.showMessageDialog(null, "Datos Guardados");
            }
       } catch (SQLException ex) {
           Logger.getLogger(Enfermera.class.getName()).log(Level.SEVERE, null, ex);
       }
         desabilitar();   
         btn_sacarcita.setEnabled(true);
    }//GEN-LAST:event_btn_guardardatosActionPerformed

    private void btn_sacarcitaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_sacarcitaActionPerformed
        SimpleDateFormat sdf=new SimpleDateFormat("yyyy-MM-dd");
        String date=sdf.format(date_calendario.getDate());
        String estado=txt_estado.getText();
        Float monto=Float.parseFloat(txt_monto.getText());
        String turno=(String)cmb_turno.getSelectedItem();
        String especialidad=(String)cmb_especialidad.getSelectedItem();
        int dni= Integer.parseInt(txt_dnibuscar.getText());
        String SQL_SACARCITA="INSERT INTO dbclinica.citas(estado,fecha,turno,especialidad,monto,dnicliente)VALUES(?,?,?,?,?,?)";
       try {
           conexion=Conectar.getConnection();
           pstm =conexion.prepareStatement(SQL_SACARCITA);
           pstm.setString(1,estado);
           pstm.setString(2, date);
           pstm.setString(3,turno);
           pstm.setString(4, especialidad);
           pstm.setFloat(5,monto);
           pstm.setInt(6,dni);
           int n=pstm.executeUpdate();
        if(n>0){
                JOptionPane.showMessageDialog(null, "Cita Reservada");
            }
       } catch (SQLException ex) {
           Logger.getLogger(Enfermera.class.getName()).log(Level.SEVERE, null, ex);
       } 
     btn_nuevacita.setEnabled(true);
    }//GEN-LAST:event_btn_sacarcitaActionPerformed

